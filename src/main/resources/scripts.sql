--Oracle
CREATE TABLE LOCATIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    CITY VARCHAR2(100),
    STATE VARCHAR2(100),
    COUNTRY VARCHAR2(100),
    TYPE VARCHAR2(50),
    ACTIVE NUMBER(1) DEFAULT 1,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

CREATE TABLE INVENTORY (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VARIANT_SKU VARCHAR2(50) NOT NULL,
    LOCATION_ID NUMBER NOT NULL,
    AVAILABLE_QTY NUMBER DEFAULT 0 NOT NULL,
    RESERVED_QTY NUMBER DEFAULT 0 NOT NULL,

    CONSTRAINT UK_INV UNIQUE (VARIANT_SKU, LOCATION_ID),
    CONSTRAINT FK_LOCATION FOREIGN KEY (LOCATION_ID) REFERENCES LOCATIONS(ID)
);

CREATE TABLE INVENTORY_TXN (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VARIANT_SKU VARCHAR2(50),
    LOCATION_ID NUMBER,
    TYPE VARCHAR2(50),
    QUANTITY NUMBER,
    REFERENCE_ID VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

--Postgress
CREATE TABLE inventory
(
    id                 BIGSERIAL PRIMARY KEY,
    product_id         BIGINT    NOT NULL UNIQUE,
    product_name               VARCHAR(200)   NOT NULL,
    available_quantity INT       NOT NULL CHECK (available_quantity >= 0),
    reserved_quantity  INT       NOT NULL DEFAULT 0 CHECK (reserved_quantity >= 0),
    version            INT,
    last_updated       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_inventory_product_id ON inventory (product_id);

CREATE TABLE inventory
(
    id                 BIGSERIAL PRIMARY KEY,
    product_id         BIGINT    NOT NULL,
    warehouse_id       BIGINT    NOT NULL,
    available_quantity INT       NOT NULL,
    reserved_quantity  INT       NOT NULL DEFAULT 0,
    version            INT,
    last_updated       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (product_id, warehouse_id)
);